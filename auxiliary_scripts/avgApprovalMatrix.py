def main():
    cSizes = range(2, 16)
    groundTruthProbs = [round(x * 0.05, 2) for x in range(21)]
    candCount = 100
    for p in reversed(groundTruthProbs):
        avgApprovals = [
            round(float(k) * p + float((candCount - k)) * (1 - p), 2) for k in cSizes
        ]
        avgAppString = " ".join(["{:>4}".format(av) for av in avgApprovals])
        print(f"{p} \t\t{avgAppString}")


if __name__ == "__main__":
    main()
